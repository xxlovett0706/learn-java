// 八大基本数据类型
public class Demo03 {
    public static void main(String[] args) {
        // 整数
        byte num1 = 20;
        short num2 = 20;
        int num3 = 20; // 最常用
        long num4 = 20L; // long类型要在后面加上L

        // 小数：浮点数
        float num5 = 3.14F;
        double num6 = 3.14; // 最常用

        // 字符
        char name1 = 'A';
        // 字符串 String 不是关键字，而是一个类
        // String name2 = "澈水";

        // 布尔值：是否
        boolean flag = true;
        // boolean flag = false;
    }
}
