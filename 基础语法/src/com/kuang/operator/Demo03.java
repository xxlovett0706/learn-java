package com.kuang.operator;

// 关系运算符 返回的结果： 正确，错误
public class Demo03 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println(a > b); // false
        System.out.println(a < b); // true
        System.out.println(a == b); // false
        System.out.println(a != b); // true
    }
}
